<p id="para">
<font style="color:#660066;">
<i>"<b>A picture speaks a thousand words</b>"</i>
</font>

</br></br>
When it comes to a website, usability is one of the top priorities for a developer as well as the user.
</br>

Usability of a webpage is contribted by many factors such as proper text,images,audio,video,etc.
</br>

Here I will talk about <b>usability of webpage with images</b>.
</br>
</br>

Images play a crucial role in today's life.
</br>

People prefer images over text bacause They provide better and quick understanding.
</br>

Images draw attention and trigger emotional aspect of human mind unlike text.
</br>

But better web usability is attained if use of images is correct.

</br>

They should make sense with the text and the whole website.
</br>
</br>

In my website, I have used a background image which creates a unique atmosphere to give users an immediate impression.
</br></br>

Another point to keep in mind is to have appropriate image size and format.

</br>

Big images take time to load causing the website to be slow.
</br>

Usability of a webpage can be improved tremendously with the use of proper images.
</br>


