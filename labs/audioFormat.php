<h1>Different File Formats</h1>

<p id="para">

An audio file format is a file format for storing digital audio data on a computer system.</br>

It is important to distinguish between the audio coding format.</br>

There are three major groups of audio file formats:-</br>
<b>
Uncompressed audio formats</br>

Formats with lossless compression</br>

Formats with lossy compression</br>
</b>

Some common formats are aiff, flac, ogg, mp3, wav, mpeg, etc.</br>

In this webpage, I have used <b> mp3 </b>, <b>ogg </b>and <b>wav</b> file format.</br>

<b>
I chose to use these formats because currently they are the 3 supported file formats.
</b>
